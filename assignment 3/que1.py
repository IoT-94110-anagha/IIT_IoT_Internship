s = "Hello IoT World" 

print("Original string:", s)
print("Upper:", s.upper())
print("Lower:", s.lower())
print("Title:", s.title())
print("Length:", len(s))
print("Replace:", s.replace("IoT", "Python"))
print("Split:", s.split())
print("Find 'World':", s.find("World"))
print("Starts with 'Hello':", s.startswith("Hello"))
print("Ends with 'World':", s.endswith("World"))